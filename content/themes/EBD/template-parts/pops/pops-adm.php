            <br>
            • Ao identificar quaisquer erros, corrigi-los imediatamente, ou se o problema for da própria instituição financeira, fazer a solicitação da compensação dos valores indevidos de forma breve.</p>
          <p class="modal-text">
            5. Confrontando os dados da conciliação bancária:<br>
            <br>
            • Para uma conciliação eficiente, é importante verificar informações e dados dos extratos e dos controles de contas a pagar e a receber internos para verificar inconsistências.</p>
          <p class="modal-text">
            6. Principais informações que devem ser observadas:<br>
            <br>
            • Saldos bancários: conferir os valores dos saldos iniciais e finais.<br>
            <br>
            • Datas dos lançamentos: se condizem com as datas previstas de recebimento e pagamento.<br>
            <br>
            • Os valores entre o banco e o controle interno: se existem multas, juros ou descontos, e principalmente se são devidos.<br>
            <br>
            • Verificar os lançamentos não identificados, sejam eles de controle interno, sejam eles do extrato bancário.
          </p>
          <p class="modal-text">
            7. Lançamentos divergentes entre banco e controle interno:<br>
            <br>
            • Se houver divergência entre o lançamento do controle interno e do banco, buscar o documento original para analisar de onde surgiram as diferenças.<br>
            <br>
            • Com a análise feita, deverá ser verificado se houve um valor maior por juros ou descontos, ou se o lançamento foi indevido – neste caso, a correção deverá ser imediata.</p>
