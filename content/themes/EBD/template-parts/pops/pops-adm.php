          <h4 class="modal-subtitle">Responsáveis</h4>
          <p class="modal-text">Gestor Administrativo (GAD)</p>
          <h4 class="modal-subtitle">Objetivo</h4>
          <p class="modal-text">
            Ter um local específico para o armazenamento de materiais, onde sejam armazenados de forma segura e organizada,
             para que seus itens estejam facilmente disponíveis sempre que solicitados, com o controle e histórico de entrada e saída, 
             e para evitar extravios de mercadorias.
          </p>
          <h4 class="modal-subtitle">Impactos</h4>
          <p class="modal-text">Falta de materiais apropriados para a execução dos serviços dos colaboradores, diferença de estoque 
            impactando negativamente a saúde financeira da filial.
          </p>
          <h4 class="modal-subtitle">Referências</h4>
          <h4 class="modal-subtitle">Passo a Passo do POP :</h4>
          <p class="modal-text">
            1. Escolha do espaço:<br>
            <br>
            • O primeiro passo para se ter eficiência, é planejar o espaço. O local para o almoxarifado deve ser escolhido de acordo com a quantidade de produtos a 
            serem armazenados, o tamanho e a rotatividade.<br>
            <br>
            • Levar em consideração o crescimento da empresa – quanto maior a demanda, maior deverá ser a capacidade de armazenamento.
             É preciso também separar um espaço para manuseio, divisão e embalagem.<br>
            <br>
            • Manter um ambiente limpo e organizado para armazenar os itens e considerar os efeitos climáticos, como temperatura, ventilação e umidade. Certificar-se de que foi escolhido um local para fácil movimentação das mercadorias e 
            verificar como será realizada a locomoção interna (carrinho, escada, entre outros).
          </p>
          <p class="modal-text">
            2. Adequação e utilização do espaço<br>
            <br>
            • Instalar prateleiras para facilitar e aumentar a capacidade de armazenamento do almoxarifado. Devem ser dimensionadas de acordo com o produto que receberão,
             levando em consideração o peso que suportam, seu tamanho e profundidade.<br>
            <br>
            • A altura máxima das prateleiras também deve ser bem dimensionada pois, caso fiquem altas demais, necessitarão de equipamentos
             para facilitar a retirada e o armazenamento de novos produtos.<br>
            <br>
            • Os produtos de limpeza e/ou químico devem ser estocados separadamente dos produtos alimentícios e devem ter a 
              sua respectiva Ficha de Informação de Segurança do Produto Químico (FISPQ).<br>
            <br>
            • Os alimentos não podem ficar em contato com o chão.
          </p>
          <p class="modal-text">
            3. Padronize as mercadorias:<br>
            <br>
            • Cadastrar todos os materiais no sistema antes de armazenar, garantindo que sempre seguirão um padrão para facilitar que sejam encontrados.<br>
            <br>
            • Sinalizar cada uma das prateleiras indicando qual é o produto armazenado ali, seu código e categoria, facilitando a retirada e o controle maior das quantidades do que foi recebido em relação às quantidades que realmente existem no estoque.
          </p>
          <p class="modal-text">
            4. O produto mais antigo deve sair antes que o novo, garantindo que a entrega seja feita corretamente e que não ocorra desperdício de materiais que já venceram e que não poderão mais ser utilizados.
          </p>
          <p class="modal-text">
            5. Registro de entrada e saída de estoque:<br>
            <br>
            • Toda a movimentação deve ser registrada via sistema, nenhum deslocamento deve ser feito sem registro, nem mesmo quando o produto não permanece no estoque por muito tempo.<br>
            <br>
            • Registro de cadastro: realizar o cadastro dos fornecedores e produtos, o fornecedor deve estar vinculado ao consumo na base de cadastro realizado pelo TI, caso não esteja, deve solicitar o vínculo do responsável. No momento do cadastro da descrição do produto, o responsável deve vincular o código da filial à sua descrição. Ex.: (01) CANETA AZUL, para facilitar a busca no momento da entrada, saída ou baixa.<br>
            <br>
            • Registro do pedido de compra (Pré-Entrada): realizar com base nas informações
             contidas na Nota Fiscal e códigos gerados no momento do cadastro para os fornecedores e produtos, sempre utilizando o código da sua própria filial, incluindo os lançamentos e uma pré-conferência dos valores, tributação, data do vencimento e forma de pagamento.<br>
            <br>
            • Registro de entrada: verificar se as quantidades dos produtos, valores, tributação, data do vencimento e forma de pagamento estão corretas conforme Nota Fiscal, gerando as contas a pagar.<br>
            <br>
            • Cancelamento da nota: caso seja identificado algum erro de lançamento após ser realizada a entrada, fazer o cancelamento via sistema, informando o código da filial, número da nota, código do fornecedor e data de entrada. Após o cancelamento, iniciar novamente o processo de Registro do pedido de compra (Pré-Entrada) e o Registro de entrada.<br>
            <br>
            • Solicitação de materiais: consumo interno (materiais de informática, escritório, produtos de limpeza, descartáveis, alimentícios, uniformes etc.), deverão ser solicitadas via e-mail ao responsável.<br>
            <br>
            • O responsável deverá realizar o registro da solicitação recebida por e-mail para registrar a saída e entrega dos produtos solicitados, seguindo os seguintes passos: Nova Solicitação, Gerenciar Solicitações e Atender Solicitações.<br>
            <br>
            • O responsável deverá realizar o registro de perdas/avarias e seus motivos quando ocorrerem.
          </p>
          <p class="modal-text">
            6. Fazer inventários periódicos:<br>
            <br>
            • A realização de inventários completos (estoque total) deve ser realizada mensalmente, verificando os saldos em estoque em relação aos saldos contábeis.<br>
            <br>
            • Sempre que necessário realizar o método de conferência rotativa, com objetivo de conferir um ou mais produtos específicos, verificando os saldos em estoque em relação aos saldos contábeis, mesmo que fora do período do inventário.<br>
            <br>
            • As impressões da listagem de estoque para a contagem e lançamento no sistema devem ser realizadas em via cega, não podendo conter as quantidades contábeis.<br>
            <br>
            • Após realizada a contagem, realizar o processo de digitar as contagens.<br>
            <br>
            • Aplicar a contagem e analisar se existem diferenças das quantidades contábeis em relação às quantidades físicas.<br>
            <br>
            • Caso existam diferenças, sejam elas negativas ou positivas, realizar a recontagem dos itens e novamente o processo de Digitar e Aplicar as contagens.<br>
            <br>
            • Caso as diferenças persistirem após a recontagem, realizar a baixa e/ou entrada das quantidades identificadas.<br>
            <br>
            • As baixas devem ser feitas na conta gerencial do administrativo.
          </p>
