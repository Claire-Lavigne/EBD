          <h4 class="modal-subtitle">Passo a Passo do POP</h4>
          <p class="modal-text">
              O seguro empresarial é renovado anualmente pela diretoria corporativa administrativa. 
          </p>
          <p class="modal-text">
            1. Cuidados ao contratar o seu seguro empresarial:<br> 
            <br>
            • Na hora de negociar a aquisição do serviço, contrate uma corretora de seguros para que ela auxilie a avaliar os riscos e as coberturas necessárias para o negócio. O contato face a face é importante para realizar a negociação.<br> 
            <br>
            • Contratar uma corretora, pois contam com profissionais qualificados para avaliar os mais diversos riscos que a empresa pode correr. Além disso, eles trabalham unicamente para atender ao cliente, do início ao fim do processo, ou seja, desde a negociação do contrato do serviço até o suporte no atendimento de um sinistro ou uma renovação.<br> 
            <br>
            • Após a escolha da corretora, verificar se ela tem registro na Superintendência de Seguros Privados (SUSEP).
          </p>
          <p class="modal-text">
            2. O que se deve levar em conta na hora de simular o seguro:<br> 
            <br>
            • O responsável pela contratação do seguro deve conhecer todos os detalhes do negócio. Quais são os materiais mais utilizados, o porte das máquinas, a localização do estabelecimento e como é feito o trabalho diário dos funcionários, pois todos esses itens influenciam na decisão das coberturas e nos valores do seu seguro. Conhecendo o negócio por inteiro, a corretora de seguros contratada poderá apurar, junto ao responsável, qual o tipo de cobertura mais adequado.
          </p>
          <p class="modal-text">
            3. Como escolher a cobertura certa para sua empresa:<br> 
            <br>
            • Em geral, um seguro para empresas já apresenta uma cobertura básica contra incêndios, que é obrigatória. No entanto, dependendo do tamanho e dos riscos do negócio, é preciso contratar coberturas específicas. Esse seguro é chamado de compreensivo ou multirrisco e/ou cobertura básica e adicionais. É de suma importância que o empreendedor avalie os dois serviços.<br> 
            <br>
            • Normalmente apresentam coberturas básicas como:<br> 
            <br>
            - incêndio;<br> 
            - explosão;<br> 
            - fumaça.<br> 
            • Usualmente também incluem coberturas adicionais como:<br> 
            - danos elétricos;<br> 
            - contaminação e deterioração de mercadorias em ambiente refrigerado;<br> 
            - desmoronamento;<br> 
            - despesas fixas;<br> 
            - despesas com instalação em novo local;<br> 
            - equipamentos eletrônicos com ou sem subtração;<br> 
            - equipamentos estacionários;<br> 
            - equipamentos móveis e empilhadeiras;<br> 
            - tumultos, derrame e vazamento;<br> 
            - desentulho e desmoronamento;<br> 
            - queda de aeronaves, impacto de veículo e fumaça;<br> 
            - alagamento e inundação;<br> 
            - roubo de valores;<br> 
            - roubo de bens;<br> 
            - roubo de mercadorias.
          </p>
          <p class="modal-text">
            4. Mapeamento:<br> 
            <br>
            • O Gestor Administrativo (GAD) deve garantir que seja realizada a contabilização em valores dos imobilizados da empresa.<br> 
            • Realizar o preenchimento correto da planilha enviada pela diretoria administrativa.<br> 
            • Enviar e-mail conforme a data estipulada pela diretoria.<br> 
            • São exemplos de ativos imobilizados:<br> 
            - terrenos;<br> 
            - edificações;<br> 
            - móveis e utensílios;<br> 
            - máquinas e equipamentos;<br> 
            - ferramentas;<br> 
            - computadores e periféricos.
          </p>
          <p class="modal-text">
            5. Documentação:<br> 
            <br>
            • O GAD deve ter uma cópia da apólice de seguro vigente.
          </p>
          <p class="modal-text">
           6. Em caso de sinistro, comunicar imediatamente à diretoria corporativa administrativa.
          </p>
