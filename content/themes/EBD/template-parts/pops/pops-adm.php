          <p class="modal-text"><b>Checklist – ADM – Estrutura Física</b>.</p>
          <h4 class="modal-subtitle">Passo a Passo do POP</h4>
          <p class="modal-text">
          Deve ser realizado, mensalmente, uma avaliação técnica com o objetivo de diagnosticar falhas ou pontos de melhoria em relação à conservação predial, segurança e higiene. Dessa forma, teremos sempre um ambiente de trabalho seguro, livre de contaminações e infestações de pragas, garantindo a qualidade dos produtos e o bem-estar dos nossos colaboradores.</p>
          <p class="modal-text">
          1. Áreas externas:<br>
          <br>
          • O perímetro da filial (terreno) deve estar devidamente murado e/ou gradeado em toda a sua extensão, impedindo o livre acesso às dependências da filial.<br>
          <br>
          • A iluminação deve ser uniforme. Quando for de LED, deve ter proteção contra queda, os demais tipos de lâmpadas devem ter proteção contra queda e explosão. As instalações elétricas devem estar embutidas e, quando externas, devem estar protegidas por tubulações.<br>
          <br>
          • O transformador de energia, quando interno, deve estar em local isolado, não permitindo o livre acesso e com as revisões e verificações do nível de óleo em dia.<br>
          <br>
          • Filiais com banco de baterias (nobreak) devem estar em local adequado, fora das dependências dos escritórios e armazém, em local coberto, arejado e sem incidência direta de raios solares, impedindo o livre acesso.<br>
          <br>
          • As áreas devem estar livres de focos de insalubridade, tais como lixo, objetos em desuso, equipamentos, animais, poeira, água estagnada, e de vetores e pragas urbanas.<br>
          <br>
          • Paletes e prateleiras utilizadas para o armazenamento de produtos, mesmo que esporadicamente, não podem ser alocadas na área externa (ao ar livre).<br>
          <br>
          • As passagens, calçadas (internas e externas) e paredes devem ser mantidas livres, limpas e conservadas para facilitar o acesso e o controle de pragas.<br>
          <br>
          • A grama, quando houver, deve ser mantida aparada a fim de não se constituir em um foco de proliferação de pragas, assim como podas de árvores, arbustos etc.<br>
          <br>
          • Os pisos nos quais há movimentação de veículos, devem ser executados com material asfáltico ou poliédrico (paralelepípedos, blocos de cimento e areia, placas de concreto ou similar) evitando, assim, o acúmulo de detritos que sujem as dependências do armazém, além de permitir uma boa movimentação dos veículos.<br>
          <br>
          • Os ralos e grelhas externos devem permanecer limpos, desobstruídos e com telas de 7mm para proteção contra pragas.</p>
          <p class="modal-text">
          2. Áreas internas:<br>
          <br>
          • O piso deve ser constituído de material liso, antiderrapante, resistente, impermeável, lavável, íntegro, sem trincas, vazamento e infiltrações. Deve ter inclinação em direção aos ralos, não permitindo que a água fique estagnada. Em áreas com ralos, esses devem ser sifonados, dotados de dispositivos que permitam o seu fechamento, evitando a entrada de pragas.<br>
          <br>
