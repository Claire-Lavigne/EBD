          <h3 class="modal-title">POP – ADM – SEGURANÇA PATRIMONIAL</h3>
          <h4 class="modal-subtitle">Responsáveis</h4>
          <p class="modal-text">Gestor Administrativo (GAD)</p>
          <h4 class="modal-subtitle">Objetivo</h4>
          <p class="modal-text">
            Prevenir perdas e resguardar interesses da empresa no que se refere aos recursos patrimoniais e humanos existentes na estrutura física (representado por instalações, equipamentos, materiais, informações, mercadorias, pessoas etc.).
          </p>
          <h4 class="modal-subtitle">Impactos</h4>
          <p class="modal-text">
              A empresa que não tenha segurança patrimonial está mais vulnerável a que ocorram perdas financeiras ocasionadas por furtos, assaltos/roubos, sequestro, depredação etc.
          </p>
          <h4 class="modal-subtitle">Referências</h4>
          <p class="modal-text">
          <b>POP – JUR – Gestão de Contratos</b>.
          </p>
          <h4 class="modal-subtitle">Passo a Passo do POP :</h4>
          <p class="modal-text">
            1. Como garantir a segurança patrimonial:<br> 
            <br>
          • Realizar o dimensionamento correto de um sistema de segurança patrimonial (aquele que será capaz de prevenir riscos e evitar ocorrências) parte de uma análise preliminar que considera todos os fatores expostos e os pontos mais vulneráveis nas rotinas da organização.<br> 
          <br>
          • Identificar quais medidas precisam ser adotadas, sempre levando em conta as normas estabelecidas, os equipamentos que devem ser instalados e a formação da equipe encarregada pela segurança. Nesse sentido, é preciso considerar basicamente os seguintes princípios:<br> 
          <br>
          - <b>Prevenção</b>: os expedientes de segurança devem ser capazes de prevenir contra tudo aquilo que possa afetar negativamente os processos da organização.<br> 
          <br>
          - <b>Inibição</b>: o sistema de segurança deve ter caráter preventivo de forma a inibir os criminosos de atuarem no local. Analisando externamente o local, o sistema de segurança deve demonstrar que quaisquer práticas criminosas serão extremamente arriscadas.<br> 
          <br>
          - <b>Capacidade de reação</b>: caso a prevenção e a inibição não sejam suficientes para impedir uma ação criminosa, o sistema deve prever a reação para minimizar a ação dos criminosos, acionando um plano estratégico de combate à invasão e alertando os órgãos públicos de segurança.<br> 
          <br>
          - <b>Treinamento</b>: os procedimentos de rotina e aqueles que precisam ser adotados em casos de ocorrências devem ser realizados de maneira consciente, ágil e precisa, o que somente pode ser proporcionado a partir de um treinamento adequado.<br> 
          <br>
          - <b>Investimento</b>: deve ser proporcional aos riscos identificados.<br> 
          <br>
          - <b>Medidas</b>: poderão ser alterados os processos da organização em prol da segurança de pessoas e patrimônio.<br> 
          <br>
          - <b>Eficiência</b>: todos os envolvidos da equipe devem estar plenamente habilitados para cumprir as funções delegadas.<br> 
          <br>
          - <b>Integração</b>: o departamento encarregado pela segurança deve estar completamente integrado aos demais setores da organização.<br> 
          <br>
          - <b>Transparência</b>: todo e qualquer procedimento deve ser compreendido, admitido e aprovado internamente por todos os envolvidos no processo.<br> 
          <br>
          - <b>Sigilo</b>: as informações contidas no Plano de Segurança devem ser restringidas exclusivamente às pessoas envolvidas no processo, impedindo ao máximo que o processo de segurança se torne público.
          </p>
          <p class="modal-text">
            2. O sistema de segurança patrimonial deverá cumprir funções gerais de prevenção e a avaliação deve ser feita antes da efetiva implantação. É preciso observar, portanto, as particularidades de cada instalação, analisando os riscos e a respectiva necessidade de prevenção contra as seguintes possibilidades de ocorrências:<br> 
            <br>
          - incêndios;<br> 
          - furtos internos e externos;<br> 
          - assaltos;<br> 
          - atos de espionagem e concorrência desleal;<br> 
          - violação de sistemas informatizados;<br> 
          - atos de terrorismo;<br> 
          - sabotagens e paralisações intencionais de processos;<br> 
          - chantagens;<br> 
          - greves violentas;<br> 
          - uso de álcool e drogas no ambiente de trabalho;<br> 
          - epidemias e contaminações coletivas;<br> 
          - acidentes, explosões e desabamentos;<br> 
          - sequestros de dirigentes, entre outros. 
          </p>
          <p class="modal-text">
            3. Exigências para contratação de empresa prestadora de serviços:<br> 
            <br>
            • Garantir que seja elaborado um contrato, conforme <b>POP – JUR – Gestão de Contratos</b>. <br>
            <br>
            • Portaria de autorização de funcionamento expedida pela Polícia Federal por meio do Ministério da Justiça.<br> 
            <br>
            • Revisão de autorização de funcionamento fornecida anualmente pela Polícia Federal, a fim de confirmar que a empresa continua apta a operar em sua atividade.<br> 
            <br>
            • Segurança armada. Solicitar cópia dos registros das armas em nome da empresa de segurança privada para comprovação da respectiva regularidade e de que pertencem à empresa de vigilância.<br>
            <br> 
            • Curso de Reciclagem (atualização) bianual dos vigilantes expedido pelas Escolas de Formação e registrados na Polícia Federal.<br> 
            <br>
            • Apresentação das certidões de antecedentes criminais dos vigilantes que irão trabalhar junto aos postos de serviço.<br> 
            <br>
            • Exames de saúde física dos vigilantes, bem como o exame psicológico.<br> 
            <br>
            • Certidões negativas de FGTS, Impostos Municipais, Estaduais e Federais.<br> 
            <br>
            • Comprovante de recolhimento da Contribuição Sindical do exercício atual (GRCS).<br> 
            <br>
            • Cópia da Convenção Coletiva de Trabalho da categoria registrada no Ministério do Trabalho e Emprego (M.T.E.), fornecida pelo Sindicato Laboral e/ou Patronal, que comprova os benefícios e concessões estabelecidas pelas partes.<br> 
            <br>
            • Os vigilantes devem ter a Carteira Nacional de Vigilante (CNV), documento emitido pela Polícia Federal que certifica que o vigilante está em dia com as obrigações legais exigidas pela função, devendo sempre portar esse documento.
          </p>
          <p class="modal-text">
            4. Todas as certificações da empresa de vigilância, bem como as de seus funcionários, devem estar vigentes e arquivadas na área ADM.
          </p>
          <p class="modal-text">
            5. Segurança perimetral:<br> 
            <br>
            • É essencial garantir a capacidade de proteção contra violações e acessos não autorizados. Portanto, conseguir detectar com agilidade e eficiência a presença de indivíduos ou veículos suspeitos nas áreas próximas da organização ou até mesmo em locais onde eles não deveriam estar.<br> 
            <br>
            • Estabelecer o máximo de dificuldades possíveis entre a área externa e a área interna a ser protegida. Isso deve ser feito projetando sistemas físicos e eletrônicos de barreira perimetral. Muros altos, cercas elétricas, sensores perimetrais, concertinas ou ainda espetos que são alguns dos dispositivos a serem considerados analisando-se a particularidade de cada local.<br> 
            <br>
            • No caso da violação da barreira, o sistema projetado deverá permitir a pronta identificação. No caso da cerca elétrica, o rompimento do fio aciona o alarme ou também por meio de sensores perimetrais que permitem fazer essa identificação. Câmeras inteligentes com Video Analytics podem auxiliar nesse processo.
          </p>
          <p class="modal-text">
            6. Circuito Fechado de Televisão (CFTV):<br> 
            <br>
            • Garantir a instalação de câmeras de vigilância na filial e que todas estejam ativas e funcionais.<br> 
            <br>
            • O sistema de câmeras (CFTV) é um dispositivo fundamental dentro do processo de segurança eletrônica. Apresenta basicamente três funções:<br> 
            <br>
            - <b>Inibição</b>: o fato das pessoas terem conhecimento que estão sendo filmadas e eventualmente gravadas diminui a propensão de praticarem atos delituosos. Estudos demonstram que áreas monitoradas por câmeras comprovam incidência criminais menores.<br> 
            <br>
            - <b>Detecção</b>: o sistema de CFTV permite detectar ações criminosas. Isso pode ser feito pela Central de Monitoramento que por meio de um operador identifica eventuais problemas. É possível utilizar também ferramentas do tipo Video Analytics, que permite a detecção de situações de risco de forma automática.<br> 
            <br>
            - <b>Identificação</b>: mesmo que de alguma forma a detecção no momento do delito ou problema não seja efetivo – por questões tecnológicas ou de orçamento – se o sistema dispor de gravação local ou remota, é possível identificar os criminosos, o modus operandi da ação, e assim corrigir eventuais falhas no processo de segurança.
          </p>
          <p class="modal-text">
            7. Controle de acesso:<br> 
            <br>
            • Tanto a entrada como a saída de pessoas e veículos deve ser rigorosamente controlada.<br> 
            <br>
            • Fazer o registro da entrada e saída, realizando distinção entre o próprio pessoal da organização, terceirizados, visitantes e fornecedores.<br> 
            <br>
            • Implementar o controle de acesso de veículos e pedestres, contendo as informações mínimas (nome, RG, veículo, placa, data, horário de entrada e saída).
          </p>
          <p class="modal-text">
            8. Para as filiais alocadas em condomínio, serão verificadas apenas a documentação vigente da empresa de segurança condominial e o monitoramento por câmeras.
          </p>
          <p class="modal-text">
            9. Item orientativo:<br> 
            <br>
            • Realizar cotações para monitoramento externo com empresas especializadas em monitoramento 24 horas.
          </p>
