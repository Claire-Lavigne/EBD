             Proteger a empresa contra danos ao prédio e ao seu conteúdo, como máquinas, móveis, utensílios, mercadorias, além do risco de roubo em grande escala, outras ameaças que podem gerar grandes prejuízos – não só material como humano –, assim como acidentes com empilhadeiras, incêndios, acidentes naturais (enchentes, raios etc.), garantindo o pagamento/indenização previsto em contrato.
          </p>
          <h4 class="modal-subtitle">Impactos</h4>
          <p class="modal-text">
             Sem o seguro empresarial, a empresa assume os riscos financeiros e patrimoniais que possam ocorrer em virtude de acidentes, sejam eles gerados por falha humana ou mecânica, sejam por causas naturais (enchentes, raios, tremores de terra etc.).
          </p>
