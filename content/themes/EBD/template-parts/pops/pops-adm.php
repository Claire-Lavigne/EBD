          <p class="modal-text">
            10. Depósitos não identificados (DNI):<br>
            <br>
            • É muito importante ter um controle adequado, pois valores não identificados não podem ser conciliados; por outro lado, precisam de atenção para que sejam identificados o quanto antes.<br>
            <br>
            • Estar sempre atento aos valores não identificados, pois eles podem ser de uma venda não reconhecida, de um erro na transferência ou ainda de antecipação de cliente via deposito bancário.<br>
            <br>
            • É importante que, posteriormente, esse depósito seja identificado, não deixando que gere saldo DNI de um mês para o outro.</p>
          <p class="modal-text">
            11. Conciliação diária:<br>
            <br>
            • Entrar na <b>Rotina 604 – Conciliar</b>, selecionar a filial, o caixa/banco a ser conciliado, e todas as moedas que tenham movimento no dia da conciliação, uma a uma.<br>
            <br>
            • Iniciar a conciliação dos valores com apoio do extrato bancário e/ou relatório da <b>Rotina 605 – Extrato Caixa/Banco</b> de todos os documentos de movimentações de entradas e saídas de valores (relatório de cargas <b>Rotina 408 – Carga fechada</b> por usuário, extrato de vales, recibo de adiantamentos, borderô de pagamento, comprovantes de depósito no acerto do carregamento, relatório CNAB400, baixa de DNI).<br>
            <br>
            • Verificar se o saldo conciliado está de acordo com os documentos verificados.<br>
            <br>
            • Imprimir e salvar o relatório da <b>Rotina 605 – Extrato Caixa/Banco</b> após realizada a conciliação de todas as moedas e bancos.<br>
            <br>
            • Armazenar o documento base para organização e posterior envio de documentos ao contador.</p>
