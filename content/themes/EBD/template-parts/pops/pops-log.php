      <div class="modal-body">
        <h3 class="modal-title">POP – LOG – ESTRUTURA DA CÂMARA FRIA</h3>
        <h4 class="modal-subtitle">Responsáveis</h4>
        <p class="modal-text">Gestor da Logística (GL)</p>
        <h4 class="modal-subtitle">Objetivo</h4>
        <p class="modal-text">Ter uma câmara fria corretamente planejada e construída conforme parâmetros específicos para o uso adequado do frio, destinada à guarda de mercadorias perecíveis e ou termossensíveis, garantindo a qualidade dos produtos nela armazenados.
        </p>
        <h4 class="modal-subtitle">Impactos</h4>
        <p class="modal-text">Quando a câmara fria não for construída respeitando especificações técnicas necessárias de acordo com a aplicação esperada, poderá não preservar as características originais dos produtos que requer esse modelo de armazenamento.
        </p>
        <h4 class="modal-subtitle">Referências</h4>
        <p class="modal-text">
          Manual – LOG – Boas Práticas.<br>
          POP – JUR – Gestão de Contratos.<br>
          <i>Checklist</i> – LOG – Estrutura da Câmara Fria.
        </p>
        <h4 class="modal-subtitle">Passo a Passo do POP</h4>
        <p class="modal-text">A câmara fria deve ter isolamento térmico apropriado, equipamentos de refrigeração e de controle competente para manter a temperatura num patamar apropriado ao tipo de produto nela armazenado. Para isso, seguem as definições básicas para uma câmara fria adequada:
        </p>
        <p class="modal-text">1. Especificações/características para câmara fria:<br>
          <br>
          <br>
          • Gabinete: é a estrutura física, paredes e teto, composto por placas desmontáveis de painel frigorífico autoportante, revestido de material lavável.<br>
          <br>
          • Porta: estrutura física com isolamento térmico que permite o acesso de pessoas e equipamentos ao interior da câmara fria.<br>
          <br>
          • Equipamento de refrigeração: responsável pela geração e manutenção do frio no interior da câmara com equipamentos integrados por condensadora, evaporadora e o painel de controle de funcionamento.<br>
          <br>
          • Acessórios: orienta-se a cortina termoplástica para conter o frio no interior e impedir a entrada de umidade quando a porta estiver aberta, entre outros.
        </p>
        <p class="modal-text">2. Iluminação: <br>
          • Quando for de LED, deve apresentar proteção contra queda, assim como os demais tipos de lâmpadas devem ter proteção contra queda e explosão.
        </p>
        <p class="modal-text">3. A câmara fria deve ser produzida e instalada por empresa especializada que definirá as características e especificações corretas de acordo com a aplicação esperada. A correta aplicação desse espaço deve garantir, no mínimo, as seguintes orientações.
        </p>
        <p class="modal-text">4. Níveis de temperatura e umidade: <br>
          <br>
          <br>
          • No que tange às temperaturas e umidade, as câmaras frias devem respeitar as especificações das indústrias para cada tipo de produto.<br>
          <br>
          • Todos esses espaços precisam ter o controle de temperatura, que deve ser feito de maneira eletrônica ou manual.<br>
          <br>
          • Efetuar medições a cada três horas durante o período de jornada da filial, ter registro das medições arquivados durante doze meses.<br>
          <br>
          • Realizar a certificação da calibração de todos os equipamentos de refrigeração e medidores de temperatura e umidade, no máximo a cada doze meses.<br>
          <br>
          • Ter medidor digital termo-higrômetro reserva para medição de temperatura e umidade do ambiente da câmara fria. O termômetro digital infravermelho somente é destinado para medição de temperatura dos produtos.
        </p>
        <p class="modal-text">5. Espaço físico:<br>
          <br>
          • A capacidade interna da câmara fria, sala climatizada ou antecâmara e câmara de congelados, deve ser suficiente para armazenar o estoque de giro por pelo menos um mês. Quando não houver espaço suficiente para armazenamento, o GL não poderá receber a carga sem a validação da Diretoria. O armazenamento em locais inapropriados não atenderá o <b>Quesito – LOG. – Estrutura da Câmara Fria.</b><br>
          <br>
          • Os produtos devem estar empilhados e armazenados de forma organizada, oferecendo fácil localização e manuseio.
        </p>
        <p class="modal-text">6. Limpeza e Conservação:<br>
          <br>
          • A câmara fria, câmara de congelados ou antecâmara devem ser mantidas em bom estado de conservação, limpas, com boa vedação, pisos, paredes e teto sem rachaduras e/ou quebrados, desobstruídas de produtos estranhos. O teto e equipamentos devem estar ausentes de goteiras.<br>
          <br>
          • Ter controle de limpeza que comprove a periódica manutenção, atendendo às necessidades dos usuários, em todas as áreas destinadas à logística, arquivar por doze meses.
        </p>
        <p class="modal-text">7. Plano de Contingência:<br>
          <br>
          • Definir e implantar o plano de contingência para problemas com falta de energia ou com o próprio equipamento.
        </p>
        <p class="modal-text">8. Gerador de energia:<br>
          <br>
          • É obrigatório ter um gerador para câmara fria com faixa de temperatura para congelados. <br>
          <br>
          • Para câmara fria com faixa de temperatura para refrigerados e/ou resfriado, é indicado o recurso de geradores de energia. <br>
          • Testar esse equipamento, no mínimo, mensalmente para garantir seu funcionamento.
        </p>
        <p class="modal-text">9. Manutenção:<br>
          <br>
          Ter contrato com empresa especializada nesse ramo conforme orientações descritas no <b>POP – JUR – Gestão de Contratos</b>, para manutenção preventiva e corretiva dos equipamentos.
        </p>
        <p class="modal-text">10. Captação de água:<br>
          <br>
          • A captação de água dos condensadores dos equipamentos de refrigeração deve ser feito em recipientes fechados com abertura somente para passagem do cano ou mangueira de captação. A conexão do coletor e o recipiente deve estar devidamente vedado.<br>
          <br>
          • A utilização dessa água é destinada exclusivamente para limpeza.
        </p>
