        <p class="modal-text">2. O Gestor de Logística (GL) deverá realizar a identificação e a rastreabilidade dos itens não conformes correspondentes à solicitação, considerando que este pode ou não estar em estoque.
        </p>
        <p class="modal-text">3. Para identificar se o produto tem no estoque da filial:<br>
          <br>
          • Acessar a Rotina 1707 e realizar a opção de “Manutenção da conferência cega”.<br>
          • Selecionar o código da filial e código do produto para realizar a pesquisa.<br>
          • Identificar pelo campo da data de validade se há o produto em estoque.
        </p>
        <p class="modal-text">4. Quando houver o produto no estoque, será apresentada a quantidade disponível e a data da entrada da Nota Fiscal (NF):<br>
          <br>
          <br>
          • Com essas informações, acessar a Rotina 1707 e realizar novamente a opção de “Manutenção da conferência cega”.<br>
          <br>
          • Selecionar o intervalo de conferência com a data de entrada da NF.<br>
          <br>
          • Na guia “Conferência”, procurar o número da NF que é apresentada na guia “Notas NF”.<br>
          <br>
          • Na guia “Produtos”, é apresentado a quantidade de produtos que foi recebida com a data de validade de referência.<br>
          <br>
          • Acessar a Rotina 1118, utilizar a funcionalidade da guia “Extrato”, selecionando o período com data anterior à entrada da NF e gerar o relatório “Sintético” para identificar o saldo de estoque antes da entrada do referente produto.<br>
          <br>
          • Identificar, com base na quantidade de saída em relação ao saldo anterior, se os produtos recebidos estão em sua totalidade dentro do armazém ou a partir de qual dia tal produto começou a ser enviado para o mercado.<br>
          <br>
          • Caso não tenha ocorrido saída dos produtos em questão, localizá-los dentro do armazém, separar dos demais produtos e identificar como “Produto aguardando recolhimento da indústria devido recall”.<br>
          <br>
          • Se os produtos em questão já tiveram saído da filial, utilizar a Rotina 1118, ainda na guia “Extrato”, selecionando o período a partir da data de saída do produto e gerar o relatório “Analítico”.<br>
          <br>
          • Extrair os dados gerados e adicionar às informações geradas na Rotina 323 para ter uma relação com todas as informações necessárias para retirar os produtos do mercado.
        </p>
        <p class="modal-text">5. Quando não houver o produto no estoque:<br>
          <br>
          • Com base no <i>Shelf-Life</i> do produto, identificar em qual data foi produzido o lote em questão.<br>
          <br>
          • Acessar a Rotina 218, aplicar os filtros filial, fornecedor e produto, e emitir o relatório no período a partir da produção do produto.<br>
          <br>
          • Com as informações das datas de entrada dos produtos e números das NFs, acessar a Rotina 1710, realizar a opção de “Manutenção da conferência cega” e executar a consulta para cada dia de entrada dos produtos, separadamente.<br>
          <br>
          • Na guia “Conferência”, procurar o número da NF que é apresentada na guia “Notas NF”.<br>
          <br>
          • Na guia “Produtos”, é apresentado a quantidade de produtos que foi recebida com a data de validade de referência.<br>
          <br>
          • Acessar a Rotina 1118, utilizar a funcionalidade da guia “Extrato”, selecionando o período com data anterior à entrada da NF e gerar o relatório “Sintético” para identificar o saldo de estoque antes da entrada do referente produto.<br>
          <br>
          • Identificar, com base na quantidade de saída em relação ao saldo anterior, se os produtos recebidos estão em sua totalidade dentro do armazém ou a partir de qual dia esse produto começou a ser enviado para o mercado.<br>
          <br>
          • Utilizar a Rotina 1118, ainda na guia “Extrato”, selecionando o período a partir da data de saída do produto e gerar o relatório “Analítico”.<br>
          <br>
          • Extrair os dados gerados e adicionar às informações geradas na Rotina 323 para ter uma relação com todas as informações necessárias para retirar os produtos do mercado.
        </p>
        <p class="modal-text">6. Retirada emergencial: <br>
          <br>
          • O GL deve organizar a força tarefa envolvendo a equipe de distribuição com veículo e pessoal exclusivo para esta finalidade, em conjunto com a equipe Comercial e de Merchandising para garantir a coleta imediata.<br>
          <br>
          • Solicitar no cliente o bloqueio para a venda e que seja realizada a identificação.<br>
          <br>
          • O processo deve ser feito pela distribuidora dentro do prazo de vinte e quatro horas.
        </p>
        <p class="modal-text">7. Para a realização de um recall de qualidade, o GL deverá ter domínio das seguintes informações:<br>
          <br>
          • Código do lote, data do faturamento, quantidade envolvida, data de validade do lote.<br>
          • Data da entrada dos produtos e saldo em estoque dos produtos em questão. <br>
          • Volume de venda diário. <br>
          • Vendas ocorridas antes e após a data de recebimento dos produtos. <br>
          • Número das NFs de venda desses produtos.<br>
          • Dados dos clientes: razão social, CNPJ, endereço, telefone.<br>
          • Quantidade de venda por cliente.
        </p>
        <p class="modal-text">8. Simulados <br>
          <br>
          • As filiais deverão realizar, anualmente, todos os simulados de recall solicitados pelo Departamento de Qualidade & Processos.
