      <div class="modal-body">
        <h3 class="modal-title">POP – LOG – BAIXA DE IMPRÓPRIOS</h3>
        <h4 class="modal-subtitle">Responsáveis</h4>
        <p class="modal-text">Gestor da Logística</p>
        <h4 class="modal-subtitle">Objetivo</h4>
        <p class="modal-text">Controle e redução dos valores nas baixas de impróprios e evitar o acúmulo dos produtos impróprios à comercialização e consumo, garantindo a sua correta destinação.
        </p>
        <h4 class="modal-subtitle">Impactos</h4>
        <p class="modal-text">Elevação dos custos afetando a margem e a rentabilidade.
        </p>
        <h4 class="modal-subtitle">Referências</h4>
        <p class="modal-text"></p>
        <h4 class="modal-subtitle">Passo a Passo do POP</h4>
        <p class="modal-text">1. Até o dia 10 de cada mês, o Gestor da Logística (GL) deve atualizar as informações dos produtos com idade crítica para o Gerente Comercial (GC), com todos os produtos com prazo de vencimento de até 120 dias.
        </p>
        <p class="modal-text">2. Diariamente, o GL deve garantir o bloqueio de todos os produtos com o prazo de vencimento inferior a 30 dias.
        </p>
        <p class="modal-text">3. O GC deve informar às equipes sobre esses produtos, e ao longo do mês atuar com ações de vendas para os produtos com mais de 30 dias para o vencimento, e negociar com as indústrias o ressarcimento dos produtos até 30 dias do vencimento.
        </p>
        <p class="modal-text">4. Caso existam negociações de preço para os produtos – conforme o item 2 – o Supervisor de Vendas (SV) deve informar à logística, o código do cliente, razão social, código e quantidade dos produtos.
        </p>
        <p class="modal-text">5. A logística deve garantir a correta separação e carregamento, respeitando as informações fornecidas pelo SV.
        </p>
        <p class="modal-text">6. Até o dia 20 de cada mês, o GC deve enviar para o GL as devidas tratativas com as indústrias.
        </p>
        <p class="modal-text">7. Sempre que existir qualquer particularidade que prorrogue a baixa ou devolução de qualquer produto dentro do mês, o gerente regional geral deve validar com a diretoria comercial, até o dia 20 do mês, os itens que serão postergados para o mês subsequente. O prazo máximo para a realização das baixas desses produtos, que fogem à regra de baixa dentro do mês vigente, deverá ser baixado ao final do segundo mês.
        </p>
        <p class="modal-text">8. Em qualquer tratativa fora da regra de baixa dentro de 30 dias, a diretoria deve definir as ações a serem adotadas e retornar para o GL da filial, até o dia 25 do mês.
        </p>
        <p class="modal-text">9. Até o fechamento do mês de competência, o GL deve garantir a baixa ou devolução de todos os produtos vencidos, juntamente às tratativas definidas pelo regional e pela diretoria corporativa.
        </p>
        <p class="modal-text">10. O GL deve garantir que todos os produtos vencidos, baixados ou não, sejam retirados do estoque de produtos para venda e destinados à área de produtos impróprios.
        </p>
        <p class="modal-text">11. Os produtos vencidos que não serão baixados no mês devem ser disponibilizados nas informações do mês subsequente.
        </p>
        <p class="modal-text">12. Feitas as devidas baixas, o responsável pela emissão das notas fiscais (NF) deve informar os números das NFs e os respectivos valores baixados de cada indústria ao responsável pela gestão da conta-corrente com as indústrias e para o GL.
        </p>
        <p class="modal-text">13. O responsável pela gestão da conta-corrente com as indústrias deve garantir o registro no controle da conta-corrente, quando negociado seu ressarcimento, e o GL deve realizar a correta destinação final dos produtos.
        </p>
