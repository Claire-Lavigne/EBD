          <br>
          <br>
          • Os produtos destinados à devolução ou descarte também devem estar separados da área de armazenamento dos produtos apropriados para comercialização, organizados, agrupados por fabricante e identificados.
           <br>
          <br>
          • Não armazenar alimentos junto aos produtos químicos, de higiene, de limpeza e perfumaria, para evitar contaminação cruzada. Assim como segregar alimentos com odores fortes, e não estocar alimentos que tenham facilidade a proliferação de pragas por proximidade. Estes devem estar separado por barreira física. Essa barreira também pode ser feita com enlatados ou por água, pois são mais resistentes a esse tipo de contaminação.
           <br>
          <br>
          • Detergentes, substâncias sanitizantes, solventes ou qualquer produto químico utilizado para limpeza dos ambientes e equipamentos da filial devem ter a FISPQ (Ficha de informação de segurança para produto químico) e ser guardados em lugar específico, fora da área de armazenamento dos produtos alimentícios.
          <br>
          <br>
          <br>
          Produtos Químicos:<br>
          <br>
          • Os produtos químicos com forte odor e/ou potencial de contaminação do ambiente e/ou demais produtos devem ter a atenção redobrada, não apenas no armazenamento, mas também no recebimento e manuseio desses produtos.
          <br>
          <br>
          • O armazenamento deve ser feito em área ventilada e apartado dos outros produtos alimentícios, buscando juntá-los somente no momento do carregamento.
          <br>
          <br>
          • Em caso de avarias, realizar a segregação em recipiente isolado, providenciando a devolução para a indústria ou o descarte correto, o mais rápido possível.
          <br>
          <br>
          <br>
          Substâncias Perigosas:<br>
          <br>
          • Nenhuma substância que possa contaminar os alimentos deve ser utilizada ou estocada nas áreas de armazenamento de alimentos.
          <br>
          <br>
          <br>
          Materiais de Merchandising:<br>
          <br>
          • Deve haver uma área específica e organizada para o armazenamento e manuseio dos materiais de merchandising e equipamentos. Essa área deve estar limpa e identificada.<br>
          <br>
          • Os materiais de merchandising e equipamentos devem estar separados dos demais produtos.<br>
          <br>
          • O material de merchandising deve ter sua identificação conforme o nome da indústria.<br>
          <br>
          <br>
          Armazenagem e Disposição de Lixos:<br>
          <br>
          • Os lixos devem ser retirados durante e ao final de cada expediente. Os recipientes para lixo dos escritórios sem tampa não podem receber produtos perecíveis, estes devem ser destinados a recipientes com tampa e com sistema de abertura por pedal.
           <br>
          <br>
          • Dentro da área de armazenagem dos produtos para revenda, todos os recipientes para destinação de resíduos devem ter tampa e sistema de abertura por pedal, e nessa área não é permitido o descarte de alimentos.
           <br>
          <br>
          • A estocagem de lixo quando for por caçamba deve estar em perfeito estado de conservação e ter tampas, quando for área destinada a este fim deve ser fechada, longe do armazém, com piso e parede que facilite a lavagem, com vedações contra pragas.
          <br>
          <br>
          • Quando for área destinada para este fim, ter o controle de limpeza que comprove a periódica manutenção, atendendo às necessidades dos usuários, em todas as áreas destinadas à logística, arquivar por doze meses.
        </p>
        <p class="modal-text">
           3. Para ter uma boa organização no armazém e manter uma operação eficiente é importante:
          <br>
          <br>
          • Realizar análise de layout de armazenamento, por meio da curva ABC das indústrias e produtos.
          <br>
          <br>
          • Iniciar este processo, obtendo os dados de um período histórico de venda (6 a 12 meses) e classificar os produtos de maior para menor volume vendas.
          <br>
          <br>
          • Depois de classificar os produtos de alto giro para os de baixo giro, selecionar os pontos de interrupção para separar "A" (alto giro), "B" (médio giro) e "C" (baixo giro).<br>
          <br>
          • Os produtos devem ser alocados de acordo com a velocidade ou frequência em que eles são movimentados para entrada e saída do armazém. Também é importante considerar o peso, em que produtos mais pesados devem estar mais próximos à área de carregamento. O produto com maior giro e volume de SKUs (Stock Keeping Unit) deve ter a distância de viagem mais curta – desde o recebimento até o armazenamento,  <i>picking</i>, conferência e carregamento.
        </p>
        <p class="modal-text">
        4. Todo o estoque deve estar endereçado e sinalizado (áreas, ruas, colunas, andares e posições), atendendo às condições de armazenamento a seguir:<br>
          <br>
          • VERTICALIZADO: verificar se os porta-pallets têm protetor de coluna e se estão em condições seguras de armazenamento, sem amassaduras, soldas, trincas ou ferrugens que possam comprometê-los.  
          <br>
          <br>
          • BLOCADO: validar os padrões de remontagem com a indústria, dado que esta modalidade oferece maior risco à operação.
        </p>
        <p class="modal-text">
        5. Na etapa de separação, conferência e expedição é importante ter atenção aos seguintes itens:<br>
          <br>
          • Ao iniciar o processo de <i>picking</i> (retirada de produto para separação), separar, obrigatoriamente, os produtos que vencem primeiro. Esse processo é chamado de FEFO, que é a abreviação das palavras em inglês<i>First Expire - First Out</i>, que pode ser traduzida para: “O Primeiro que Vence é o Primeiro que Sai”.
           <br>
          <br>
          • Na preparação do carregamento, existem produtos que serão fracionados e cujos riscos de avarias, roubos e perdas são altos. Portanto, é necessário o uso de caixas (papelão, plástica e outras) onde devem ser acomodados para facilitar a conferência e a montagem da carga. Caixas utilizadas para acondicionar produtos químicos não podem ser reaproveitadas para acondicionamento de produtos alimentícios.
           <br>
          <br>
          • Os conferentes devidamente capacitados devem checar quantidade, qualidade e vencimento dos produtos a serem carregados. Inconsistências devem ser corrigidas e registradas no romaneio de conferência para o controle de produtividade e acuracidade.
          <br>
          <br>
          • Antes de realizar o processo de carregamento dos veículos, garantir a verificação das condições higiênico-sanitárias utilizando o <b><i>Checklist</i> – LOG – Expedição de Veículos</b>.<br>
          <br>
          • No momento da expedição das cargas, os romaneios devem ser emitidos com a placa do veículo que realizará o transporte. Quando utilizado placa “fictícia” para a emissão das Notas Fiscais (NF), esta deve ser corrigida no romaneio, fazendo a correta atribuição para a saída do veículo.
           <br>
          <br>
          • O responsável pela expedição deve garantir o horário de saída dos veículos em rota, pois o atraso impactará a produtividade das entregas e a jornada de trabalho da equipe.
        </p>
