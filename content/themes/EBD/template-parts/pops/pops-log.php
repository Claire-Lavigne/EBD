      <div class="modal-body">
        <h3 class="modal-title">POP – LOG – ESTRUTURA DO ARMAZÉM</h3>
        <h4 class="modal-subtitle">Responsáveis</h4>
        <p class="modal-text">Gestor da Logística (GL)</p>
        <h4 class="modal-subtitle">Objetivo</h4>
        <p class="modal-text">Ter todos os ambientes em conformidade para evitar acidentes, livre de sujeiras, contaminações e infestações de pragas, garantindo a qualidade dos produtos, a segurança e o bem-estar dos nossos colaboradores.
        </p>
        <h4 class="modal-subtitle">Impactos</h4>
        <p class="modal-text">Risco de acidentes, infestação de pragas, contaminação de produtos, risco à saúde de funcionários e terceiros. Também podem causar a perda da representação de indústrias, refletindo diretamente na saúde financeira da empresa.
        </p>
        <h4 class="modal-subtitle">Referências</h4>
        <p class="modal-text">
          Manual – LOG – Boas Práticas.<br>
          Política – LOG – Vidros, Acrílicos e Plásticos Duros.<br>
          Layout do armazém.<br>
          Comprovantes das execuções dos serviços de desratização e desinsetização.
        </p>
        <h4 class="modal-subtitle">Passo a Passo do POP</h4>
        <p class="modal-text">1. As áreas externas comum ao entorno do armazém devem manter condições adequadas:<br>
          <br>
          • Mantidas livres de entulhos, sucatas e materiais fora de uso.<br>
          <br>
          • As passagens vizinhas às calçadas ou paredes (corredores) devem ser mantidas livres e limpas para facilitar o controle de pragas.<br>
          <br>
          • Aparar a grama, quando houver, a fim de não se constituir em um foco de proliferação de pragas.<br>
          <br>
          • As áreas devem ser iluminadas e as lâmpadas devem ser instaladas em locais distantes das portas do armazém, de modo a não se tornar um fator de atratividade de insetos noturnos para as entradas dos prédios. Em se tratando das lâmpadas, quando for de LED, devem ter proteção contra queda, assim como os demais tipos de lâmpadas devem ter proteção contra queda e explosão.<br>
          <br>
          • Os pisos nos quais há movimentação dos veículos devem ser confeccionados com material asfáltico ou poliédrico (paralelepípedos, blocos de cimento e areia, placas de concreto ou similar) evitando, assim, o acúmulo de detritos que sujem as dependências do armazém, além de permitir uma boa movimentação dos veículos.
        </p>
        <p class="modal-text">. As áreas destinadas à logística, devem manter condições adequadas de utilização, garantindo:<br>
          <br>
          <br>
          • <i>Layout</i> que permita um bom fluxo operacional em se tratando de pessoas e equipamentos.<br>
          <br>
          • O local de armazenagem deve ser ventilado e iluminado.<br>
          <br>
          • Em se tratando das lâmpadas, quando for de LED, devem ter proteção contra queda, assim como os demais tipos de lâmpadas devem ter proteção contra queda e explosão.<br>
          <br>
          • As áreas de armazenagem devem ser mantidas limpas, livres de resíduos para evitar o acesso e a proliferação de pragas. <br>
          <br>
          • Os <i>pallets</i> vazios destinados ao uso devem estar abrigados no interior do armazém, segregados da área de estocagem e movimentação, e ser periodicamente higienizados e tratados com produtos apropriados pelo controle de pragas.<br>
          <br>
          • Os <i>pallets</i> danificados devem ser retirados do armazém.<br>
          <br>
          • Deve haver uma área própria e isolada do armazém principal para os produtos recolhidos ou destinados à inutilização e descaracterização dos produtos impróprios (área de Impróprios). Essa área deve ser limpa, organizada, ter barreiras físicas contra pragas e estar identificada como: “Área de produtos impróprios para consumo e comercialização”.<br>
          <br>
          • Deve haver uma área própria para a tratativa dos produtos destinados à reembalagem (área de <i>Repack</i>), separados por barreira física em relação às áreas de armazenagem dos produtos apropriados para comercialização.<br>
          <br>
          • Os ralos internos devem ser evitados. Se necessários, devem ser sifonados e tampados para não permitir a entrada de pragas e evitar maus odores.<br>
          <br>
          • O teto deve estar isento de vazamentos e goteiras, evitar a utilização de telhas que permitam a ocorrência de respingos.<br>
          <br>
          • O piso deve estar aplainado, liso e com as juntas de dilatação vedadas, permitindo a movimentação segura de equipamentos e acomodação dos produtos sem risco de tombamento. Para áreas com circulação de empilhadeiras, confeccionar o piso com material resistente, próprio para essa finalidade.<br>
          <br>
          • O piso e as paredes devem ser mantidos secos e sem infiltrações.<br>
          <br>
          • Para as filiais com refeitório, copa/cozinha nas dependências do armazém, é necessário que esteja asseado, disponha de mesas e cadeiras e/ou bancos em quantidades compatíveis com o número de usuários, conter geladeira, fogão e/ou forno micro-ondas para aquecimento de alimentos. Esses espaços devem ser separados das demais áreas de armazenagem de produtos, e a porta de acesso e janelas não podem ter abertura direta para o armazém. Além disso, janelas ou aberturas para as áreas externas, devem ser protegidas por uma tela milimétrica de 2mm.<br>
          <br>
          • Nos vestiários e/ou banheiros, dispor de armários individuais em bom estado de conservação, asseados, desobstruídos e com a utilização correta, separados por gêneros. Dispor de chuveiros individuais, vasos sanitários, mictórios e lavatório a razão de 1 para cada 20 usuários por turno, papel toalha e papel higiênico sempre disponíveis, lixeira com tampa acionada por pedal, dotados de saco plástico e espelhos para zelar pela boa aparência, uniformização e higiene. Esses espaços devem ser separados das áreas de armazenagem dos produtos, a porta de acesso e janelas não podem ter abertura direta para o armazém. Janelas ou aberturas desses ambientes, para as áreas externas, devem ser protegidas por uma tela milimétrica de 2mm. Os ralos devem ser sifonados e com sistema “abre e fecha” ou com tela para impedir o acesso de pragas e evitar maus odores.<br>
          <br>
          • Instalações hidráulicas em bom estado e em pleno funcionamento, no que se refere às tubulações, bombas d'água, aparelhos sanitários, lavatórios, registros, chuveiros e descargas.<br>
          <br>
          • Ter controle de limpeza que comprove a periódica manutenção, atendendo às necessidades dos usuários, em todas as áreas destinadas à logística; arquivar por doze meses.<br>
          <br>
          • Locais para guardar os materiais de limpeza, em uso ou fechados, devem apresentar a Ficha de Informação de Segurança de Produtos Químicos (FISPQ). <br>
          <br>
          • Entre tetos e paredes não deve existir aberturas, para evitar a entrada de pragas e bordas que facilitem a formação de ninhos.<br>
          <br>
          • Claraboias ou telhas translucidas são recomendadas para contribuir com a economia de energia, porém devem garantir a segurança, evitando a contaminação dos alimentos em caso de quebra.<br>
          <br>
          • Portas, janelas, claraboias, exaustores ou qualquer abertura nas dependências do armazém devem ser protegidas por uma tela milimétrica de 2mm.<br>
          <br>
          • Todas as portas, incluindo as docas, devem estar em pleno funcionamento, livre de ferrugens, amassadas ou descascadas e ter vedação de borracha ou similar.<br>
          <br>
          • Quando o estoque de materiais e equipamentos de merchandising estiver no interior do armazém, este deve ser apropriado e estar separado dos produtos de revenda, distantes de produtos químicos, identificados, limpos e conservados.<br>
          <br>
          • Todo e qualquer estrutura de vidro deve ser protegida por película conforme descrito na <b>Política – LOG – Vidros, Acrílicos e Plásticos Duros</b>, contidos no <b>Manual – LOG – Boas Práticas</b>.
        </p>
        <p class="modal-text">3. Todas as áreas:<br>
          <br>
          <br>
          • Paredes pintadas e/ou revestimentos livres de sujeiras, infiltrações, mofos etc.<br>
          <br>
          • Instalações elétricas bem executadas e com bom funcionamento (fiação isolada, interruptores, tomadas, lâmpadas, calhas fechadas, quadro de força, chaves elétricas e aparelhos elétricos bem instalados), bem como não ter fiação exposta e/ou emaranhada.
        </p>
        <p class="modal-text">4. Equipamentos:<br>
          <br>
          <br>
          • A filial que dispor de empilhadeira(s), deve ter um local adequado, limpo e segregado para a armazenagem e manutenção das baterias e cilindros de gás, conforme avaliação de técnicos de qualidade e segurança.<br>
          <br>
          • Quando esse espaço for dentro do armazém e/ou fechado por paredes, instalar exaustores protegidos por tela de 2mm.<br>
          <br>
          • O piso deve ter inclinação suficiente apenas para permitir que, em caso de vazamento da água das baterias, essa não transpasse para a área de armazenagem dos produtos, garantindo seu escoamento para ralo de sistema “Abre e Fecha”. Caso essa área não permita esse recurso, utilizar barreira física que garanta o mesmo resultado.<br>
          <br>
          • A área de armazenagens das baterias deve ser de acesso restrito.<br>
          <br>
          • Para empilhadeiras a gás, é necessário que tenham proteção de teto e sirene de ré.<br>
