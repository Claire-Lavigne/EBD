        <p class="modal-text">A não constituição da CIPA retira tanto da empresa quanto dos colaboradores uma importante ferramenta de fiscalização interna e busca pela melhoria contínua da segurança no ambiente de trabalho, tanto quanto uma CIPA constituída, porém sem atuação efetiva.
