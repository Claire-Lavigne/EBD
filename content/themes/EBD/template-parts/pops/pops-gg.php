        </p>
        <p class="modal-text">2. Caberá à equipe de RT, apoiado pela Gerência de Relações Trabalhistas e pelo Gerente Regional, garantir a eleição, constituição da CIPA e a atuação regular dos seus membros.
        </p>
        <p class="modal-text">3. Anualmente, 60 dias antes do encerramento do mandato vigente da CIPA, a equipe de RT local deverá comunicar ao Diretor ou Gerente Regional sobre o processo de eleição dos novos membros da CIPA, bem como solicitar a indicação dos membros representantes do empregador. Os colaboradores indicados pela empresa deverão ser convidados e manifestar sua decisão quanto ao convite.
        </p>
        <p class="modal-text">4. Os membros da CIPA deverão alinhar com a equipe de RT e a Diretoria ou Gerência Regional as datas de convocação das eleições, eleição e posse do novo mandato da CIPA, bem como os detalhes para realização de cada uma dessas atividades. 
        </p>
        <p class="modal-text">5. No ato da constituição e posse dos novos membros da CIPA, deverá ser discutido o planejamento anual de atividades, no qual deverá ser contemplado, no mínimo, as seguintes atividades:<br>
