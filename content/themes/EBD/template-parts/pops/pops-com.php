          <br>
          – Evolução de faturamento e positivação no ano com comparativos em relação à meta e ao ano anterior.<br>
          – Resultado de faturamento e positivação da sua área por vendedor com comparativo em relação à meta.<br>
          – Resultado de faturamento e positivação por fornecedor com comparativo em relação à meta e ao ano anterior.<br>
          – Melhores práticas da equipe.<br>
          – Assuntos específicos da equipe.<br>
          – Análise com os pontos positivos, principais problemas, suas possíveis causas e oportunidades.<br>
          <br>
          • RMV do Supervisor de Merchandising (SM):<br>
          <br>
          – Resultados de produtividade da equipe (visitas planejadas em relação às realizadas e Tempo de Execução).<br>
          – Análise de lojas com potencial de atendimento.<br>
          – Melhores práticas de execução do mês.<br>
          – Assuntos específicos da equipe.<br>
          – Análise com os pontos positivos, principais problemas, suas possíveis causas e oportunidades.<br>
          <br>
          • RMV do Gerente De Contas (GDC):<br>
          <br>
          – Evolução de faturamento, volume e positivação no ano com comparativos em relação à meta e ao ano anterior.<br>
          – Resultado de faturamento, volume e positivação da sua área por supervisor com comparativo em relação à meta.<br>
          – Análise de rupturas da indústria do mês anterior.<br>
          – Pendências da conta-corrente da indústria até o período.<br>
          – Melhores práticas da equipe.<br>
          – Assuntos específicos da indústria.<br>
          – Análise com os pontos positivos, principais problemas, suas possíveis causas e oportunidades.<br>
          <br>
          • RMG:<br>
          <br>
          – Evolução de faturamento e positivação no ano com comparativos em relação à meta e ao ano anterior.<br>
          – Resultados de faturamento e positivação por SV com comparativos em relação à meta.<br>
          – Resultados de faturamento e positivação Top 10 VDs com comparativos em relação à meta.<br>
          – Resultado de faturamento e positivação dos Top 10 fornecedores em relação à meta e em relação ao ano anterior.<br>
          – Políticas Comerciais.<br>
          – Principais direcionamentos dos fornecedores.<br>
          – Produtos com estoque alto que precisam ter foco.<br>
          – Melhores práticas de execução da sua área.<br>
          – Resultados das campanhas de incentivos.<br>
          – Recados gerais da filial.<br>
          – No dia da RMG, o Gerente Comercial poderá destinar um tempo para os fornecedores exporem os principais
          direcionamentos.<br>
          – A RMG deverá ser apresentada pelo GC e sempre que necessário abrir espaço para que os gestores das demais áreas
          possam falar dos indicadores e das ações a serem realizadas.</p>
