        <p class="modal-text">6. Os acompanhamentos devem ser atualizados semanalmente pelo Suporte Comercial de Vendas (SCV) e enviados
          por e-mail para toda a equipe do comercial. Ao encerramento de cada período dos programas de mercado, enviar
          apuração de fechamento até o 5º dia útil do mês subsequente.</p>
        <p class="modal-text">7. Os acompanhamentos devem ter uma apresentação clara dos objetivos de cada programa. Exemplo: Se o programa
          medir o atingimento por equipe, este deve conter um resumo apresentando os resultados consolidados de cada
          equipe.</p>
        <p class="modal-text">8. Os programas que tiverem atrelados às execuções de mercado, o GC, o Supervisor de Vendas (SV) e o Supervisor de
          Merchandising (SM) serão responsáveis por acompanhar os trabalhos realizados pelas equipes de vendas e promoção
          nos Pontos de Vendas (PDV) participantes do programa, garantindo excelente nível de serviço e evolução junto ao
          programa.</p>
        <p class="modal-text">9. Para os programas que preveem pagamento aos clientes e/ou pagamentos de premiações para a EBD através do
          atingimento de metas, o GC deve garantir o lançamento na conta-corrente com o fornecedor e acompanhar os
          pagamentos.</p>
        <p class="modal-text">10. Para as filiais que dispõem de GDC, as demandas referentes aos programas de mercado do fornecedor sob sua
          gestão serão de sua responsabilidade.</p>
