          Supervisor de Vendas (SV)<br>
          Coordenador de Merchandising (CM)<br>
          Supervisor de Merchandising (SM)
        </p>
        <h4 class="modal-subtitle">Objetivo</h4>
        <p class="modal-text">Apresentar as ações a serem realizadas no mês, baseando-se no planejamento estratégico da empresa, a fim de
          orientar os colaboradores de vendas e merchandising a executarem as tarefas da melhor forma possível. Além de
          apontar os erros e as ações que podem ser feitas para garantir o nível de serviço e os resultados.</p>
        <h4 class="modal-subtitle">Impactos</h4>
        <p class="modal-text">Falta de alinhamento das expectativas e das atividades inter-relacionadas das funções, áreas, departamentos e/ou
          com as indústrias. Falha na comunicação, gerando pouco ou nenhum entendimento sobre os detalhes dos planos de
          ação. Além de poder desmotivar os colaboradores por não se sentirem parte das decisões ou pela falsa percepção de
          não estarem sendo acompanhados de perto pelo seu gestor e/ou parceiro.</p>
        <h4 class="modal-subtitle">Referências</h4>
        <p class="modal-text">
          <i>Business Intelligence</i> (BI) <i>Targit anywhere</i><br>
          Planilha – EBD – Plano de ação<br>
          Planilha – COM – Lista de Presença<br>
          Planilha – COM – Ata de Reunião<br>
          Apresentação dos Fornecedores</p>
        <h4 class="modal-subtitle">Passo a Passo do POP</h4>
        <p class="modal-text">1. A preparação das reuniões deverá acontecer após o fechamento do mês para garantir que os resultados de
          fechamento estejam corretos.</p>
        <p class="modal-text">2. As reuniões devem acontecer respeitando os períodos a seguir:<br>
        <br>
          • Reunião Mensal de Planejamento Tático (RMP): até o 8° dia útil.<br>
          • Reunião Mensal de Vendas (RMV): até o 5° dia útil.<br>
          • Reunião Mensal Geral (RMG): até o 2° sábado do mês.<br>
          • Reunião Semanal de Vendas (RSV): de acordo com a organização da equipe.<br>
          • Reunião Semestral com indústria: no início de cada semestre.</p>
        <p class="modal-text">3. Deverão ser registradas na <b>Planilha – EBD – Ata de Reunião</b>, compartilhada por e-mail com os participantes.</p>
        <p class="modal-text">4. Os Gerentes De Conta (GDC), Supervisores de Vendas (SV) e Supervisores de Merchandising (SM) deverão
          apresentar o material com os resultados referentes à sua equipe.</p>
        <p class="modal-text">5. As informações utilizadas para a elaboração e atualização dos materiais deverão ser fornecidas pelo Suporte
          Comercial de Vendas (SCV) e pelo Suporte Comercial a Trade (SCT).</p>
        <p class="modal-text">6. Em caso de férias de um dos líderes, o resultado da equipe deverá ser apurado pelo SCV e encaminhado para o GC.</p>
        <p class="modal-text">7. As operações que tiverem concentração de vendedores (VD) no interior, a periodicidade acontecerá de acordo com
          o alinhamento com a Diretoria Comercial.</p>
        <p class="modal-text">8. Para a elaboração dos materiais e para que o líder se prepare de forma adequada para a apresentação, é
          importante que sejam analisados os resultados de forma detalhada pelo menos dos temas a seguir:<br>
          <br>
          • Supervisores de Vendas (SV):<br>
          <br>
          – <i>Follow-up</i> do plano de ação.<br>
          – Faturamento e positivação geral da equipe.<br>
          – Faturamento e positivação por indústria.<br>
          – Faturamento e positivação por vendedor e indústria.<br>
          – Análise do faturamento dos Top 10 clientes – ano versus anos.<br>
          – Análise do faturamento dos Top 10 clientes em declínio – ano versus anos.<br>
          – Abertura de novos clientes.<br>
          – Análise de performance das campanhas de incentivos.<br>
          – Análise da performance das ações de mercado.<br>
          – Programas de mercado dos fornecedores.<br>
          – EBDV Coaching.<br>
          – Melhores práticas: ações que impulsionaram os resultados da equipe.<br>
          – Identificação das principais oportunidades encontradas no mercado durante o período.<br>
          <br>
          • Supervisores de Merchandising (SM):<br>
          <br>
          – Produtividade de atendimento da carteira de clientes (Planejado vs Real e Tempo de Execução).<br>
          – Analisar o potencial de atendimento das lojas através da <b>Planilha – COM – Painel de Indicadores do Trade (P.I.T)</b>.<br>
          – Book de execução dos fornecedores.<br>
          – Fotos das execuções realizadas das ações de vendas negociadas no mês anterior.<br>
          – Assuntos específicos da área.<br>
          – Identificação das principais oportunidades encontradas no mercado durante o período.
