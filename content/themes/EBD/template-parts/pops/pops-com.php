          das ações e do seu acompanhamento.<br>
          <br>
          • Os indicadores tratados nessas reuniões deverão estar alinhados com a estratégia/expectativa de cada indústria,
          levando em consideração, pelo menos, o faturamento e a positivação.<br>
          <br>
          • A RSA deve ser realizada, pelo menos, com as 5 principais indústrias da filial.</p>
