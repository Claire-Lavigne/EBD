        <h4 class="modal-subtitle">Responsáveis</h4>
        <p class="modal-text">
          Gerente Comercial (GC)<br>
          Gerente De Contas (GDC)<br>
          Supervisor de Vendas (SV)<br>
          Vendedores (VD)</p>
        <h4 class="modal-subtitle">Objetivo</h4>
        <p class="modal-text">Definir a carteira de atendimento do vendedor de forma estruturada, a fim de garantir o atendimento dos clientes de
          forma planejada e eficiente e, visando o aumento de faturamento, número de visitas, aumento do nível de serviço da
          equipe de vendas e eficiência logística.</p>
        <h4 class="modal-subtitle">Impactos</h4>
        <p class="modal-text">Desequilíbrio da carteira de visitas por dia, baixa efetividade de visitas, aumento no tempo de deslocamento e
          aumento do custo logístico.</p>
        <h4 class="modal-subtitle">Referências</h4>
        <p class="modal-text">Portal Executivo Máxima – Painel de Auditoria.</p>
        <h4 class="modal-subtitle">Passo a Passo do POP</h4>
        <p class="modal-text">Para uma boa produtividade da rota de vendas, um passo fundamental é a definição do roteiro de atendimento dos
          clientes, conforme os passos a seguir:</p>
        <p class="modal-text">1. O Suporte Comercial de Vendas (SCV) deve enviar para o Gerente Comercial (GC) a base de clientes com as
          seguintes informações:<br>
          <br>
          • Código do Ponto De Venda (PDV).<br>
          • Razão social, nome fantasia do PDV e nome da rede.<br>
          • Endereço, bairro, cidade, estado, CEP.<br>
          • Data do cadastro.<br>
          • Ramo de atendimento e segmento.<br>
          • Nome do supervisor e nome do vendedor.<br>
          • Dia de visita atual, frequência de visita.<br>
          • Histórico de compra dos últimos seis meses de faturamento.<br>
          • Média mensal de faturamento.<br>
          • Média de faturamento por pedido.<br>
          • Status financeiro (inadimplência).</p>
        <p class="modal-text">2. O SCV deve validar todas as informações e realizar o saneamento da base de clientes, garantindo os seguintes
          aspectos:<br>
          <br>
          • Assim que iniciar o saneamento da base, o cadastro de novos clientes deverá ser “travado” para que os novos
          clientes não fiquem fora das análises, impactando diretamente o processo de roteirização.<br>
          <br>
          • Remover clientes sem compra a mais de 180 dias.<br>
          <br>
          • Clientes inadimplentes independentemente da data da última compra devem permanecer na base.<br>
          <br>
          • Os clientes sem compra no período entre 30 e 180 dias somente poderão ser removidos da base após validação <i>in
            loco</i> ou por ligação do Supervisor de Vendas (SV).</p>
        <p class="modal-text">3. Validação da base final de clientes pelo GC.</p>
        <p class="modal-text">4. Separar os clientes por segmentação, conforme descrito no <b>POP – COM – Segmentação por Canal</b>.</p>
        <p class="modal-text">5. O GC deverá realizar a construção das rotas, levando em consideração os seguintes aspectos:
          • Os clientes devem ser mapeados por ramos de atividade e região.<br>
          <br>
          • Concentrar os clientes de cada rota em uma região, otimizando a área de atuação do vendedor (VD).<br>
          <br>
          • Atribuir os dias de visitas, frequência e sequência de atendimento, considerando proximidade entre os PDVs,
          reduzindo tempo de deslocamento em rota.<br>
          <br>
          • Definir quais vendedores atuarão em cada rota, considerando o perfil do vendedor para cada segmentação.</p>
        <p class="modal-text">6. Durante a construção das rotas, é fundamental que sejam realizadas as análises financeiras para entender o
          potencial de venda de cada rota.</p>
        <p class="modal-text">7. Com base no potencial de faturamento de cada rota, realizar simulações de remuneração para evitar que os
          vendedores fiquem abaixo da sua média salarial.</p>
        <p class="modal-text">8. O GC deverá realizar a validação final dos roteiros antes de realizar o cadastro das novas rotas no sistema.</p>
        <p class="modal-text">9. Após a finalização do cadastro da roteirização, o SV deve apresentar a reestruturação para a equipe de vendas, a
          fim de garantir que todos tenham pleno conhecimento da sua nova base de atendimento.</p>
        <p class="modal-text">10. O SV deve verificar diariamente o cumprimento da roteirização no “Painel de Auditoria do Portal Executivo da
          Máxima”, garantindo a evolução constante dos principais indicadores a seguir:<br>
          <br>
          • Tempo Médio de Atendimento (TMA) = Tempo médio do vendedor no Ponto De Vendas (PDV). O tempo de
          atendimento é medido entre o <i>check-in</i> e o <i>check-out</i> em cada PDV.<br>
          <br>
          • Visitados = Percentual de clientes visitados em relação aos clientes planejados para visita.<br>
          <br>
          • Visitados fora da agenda = Clientes visitados e não positivados em relação aos clientes planejados para visita.<br>
          <br>
          • Positivados = Percentual dos clientes com compra em relação aos clientes planejados para visita.<br>
          <br>
          • Positivados fora da Agenda = Percentual dos clientes com compra que não estavam planejados para visita “Fora de
          rota”.<br>
          <br>
          • Positivação sobre a carteira = Percentual dos clientes com compra em relação à base total de clientes.<br>
          <br>
          • Agendados não positivados = Percentual dos clientes sem compra em relação aos clientes planejados para visita.<br>
          <br>
          • Visitados na ordem prevista = Percentual dos clientes que foram visitados na mesma ordem de atendimento
          cadastrada.</p>
        <p class="modal-text">11. Além dos indicadores apresentados no “Painel de Auditoria do Portal Executivo da Máxima”, devemos garantir a
          eficácia das visitas. O atingimento do indicador de “Eficácia” é obtido, no próprio painel, mediante a divisão dos
          clientes positivados pelos clientes visitados (positivados/visitados).</p>
        <p class="modal-text">12. O SV deve garantir acompanhamento diário de cada colaborador de sua equipe, objetivando o desenvolvimento
          das pessoas, com <i>feedback</i> justo e honesto, e orientações claras para a melhoria contínua da produtividade.<br>
          <br>
          Importante sempre orientar sobre os tópicos a seguir:<br>
          <br>
          • Garantir que no início da rota o equipamento de pedidos (tablet, celular etc.) esteja com a bateria carregada.<br>
          • Garantir que a ferramenta de vendas esteja sempre com a versão atualizada.<br>
          • Sempre manter o GPS ligado.<br>
          • Respeitar a sequência programada para as visitas.<br>
          • Realizar <i>check-in</i> assim que entrar no PDV.<br>
          • Ao final do atendimento, realizar o <i>check-out</i> antes de ir para o próximo cliente.<br>
          • Não deixar para digitar pedidos depois que sair do PDV.<br>
          • Sempre justificar na ferramenta os clientes não positivados no dia.<br>
          • Realizar transmissões dos pedidos ao longo do dia.</p>
