          desempenho, considerando:<br>
          <br>
          • Principais Itens de Controle (IC)<br>
          <br>
          – Resultado do período.<br>
          – Tendência de fechamento dos indicadores.<br>
          – Média necessária para a semana.<br>
          – Resultados de abertura de cadastro, visitação e eficácia por VD.<br>
          <br>
          • Principais direcionamentos dos fornecedores:<br>
          <br>
          – Políticas Comerciais.<br>
          – Incentivos.<br>
