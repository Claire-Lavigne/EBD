          • Analisar a remuneração variável da equipe de vendas.<br>
          • Analisar os indicadores de turnover, horas extras, banco de horas, QLP e absenteísmo.<br>
          • Acompanhar e analisar os custos de G&G e os custos com pessoal.<br>
