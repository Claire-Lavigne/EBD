          • Gente & Gestão, conforme atingimento das metas. <br>
          – Hora extra e banco de horas – <br>
          – Turnover.<br>
          – QLP.<br>
          – Absenteísmo.<br>
          – Demais indicadores tratados nas reuniões regionais e reuniões gerenciais.
          <br><br>
          • Comercial, conforme atingimento das metas.<br>
          – Faturamento.<br>
          – Positivação.<br>
          – Despesas da área.<br>
          – Margem bruta.<br>
          – Nível de Estoque.<br>
