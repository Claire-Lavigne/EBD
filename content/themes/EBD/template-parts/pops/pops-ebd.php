          • Verificação das reclassificações de despesas realizadas ao longo do mês.<br>
          <br>
          Quinzenal: <br>
          • Conta-corrente com a indústria.<br>
          <br>
          Mensal:<br>
          • Realizar o checklist de Estrutura Física conforme <b>POP – ADM – Estrutura Física</b>.<br>
          • Reunião de Planejamento Tático conforme <b>POP – EBD – Planejamento Tático</b>.<br>
          • Reuniões, no mínimo uma vez por mês, para <i>follow-up</i> dos planos de ação gerados na Reunião de Planejamento Tático.<br>
          • Realizar a crítica mensalmente de todos os contratos por meio do sistema do jurídico.
          • Perdas com crédito.<br>
          <br>
          Semestral:<br>
          • Revisões de crédito conforme <b>POP – ADM – Reavaliação do limite de crédito</b>.
