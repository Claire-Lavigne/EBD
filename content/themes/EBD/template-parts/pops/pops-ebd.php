      <div class="modal-body">
        <h3 class="modal-title">POP – EBD – PLANO DE AÇÃO</h3>
        <h4 class="modal-subtitle">Responsáveis</h4>
        <p class="modal-text">Gestores</p>
        <!-- <p class="modal-text">Gestor da Logística</p> -->
        <h4 class="modal-subtitle">Objetivo</h4>
        <p class="modal-text">
          Possibilitar que o executor siga uma sequência de tarefas mais claras e lógicas, previamente definidas, 
          conforme estratégia da área, o que leva à concretização dos objetivos de forma mais rápida, prática e segura.
        </p>
        <h4 class="modal-subtitle">Impactos</h4>
        <p class="modal-text">
          Desconhecimento das dificuldades da própria filial, atuação diretamente nos sintomas e não na causa-raiz dos problemas.
          Dificuldade no atingimento das metas, ações não sustentáveis, ficando mais sensíveis a crises econômicas e políticas, podendo sofrer com custos mais elevados.
        </p>
        <h4 class="modal-subtitle">Referências</h4>
        <p class="modal-text">
          Planilha – EBD – Plano de ação.
        </p>
        <h4 class="modal-subtitle">Passo a Passo do POP :</h4>
        <p class="modal-text">
          1. Cada gestor deve ter um plano de ação sempre que for identificado um problema (interno ou externo) e principalmente para o
           atingimento de metas, tendo sempre como objetivo principal a rentabilidade da filial. 
        </p>
        <p class="modal-text">
          2. Todo o plano de ação deve ser realizado com base no ciclo PDCA, que é uma metodologia com o objetivo de promover a melhoria contínua por meio de um ciclo de quatro ações:<br>
          <br>
          • P – <i>Plan</i> (Planejar): toda a etapa do planejamento até a definição das ações que serão adotadas.<br>
          <br>
          • D – Do (Fazer): executar os planos de ação conforme planejado.<br>
          <br>
          • C – <i>Check</i> (Checar): verificar os resultados obtidos com a realização das ações (inicia-se junto à execução).<br>
          <br>
          • A – <i>Act</i> (Agir): corrigir os planos, retomando a etapa de planejamento ou padronização do processo quando o problema estiver corrigido e as metas atingidas.
        </p>
        <p class="modal-text">
          3. O plano de ação deve seguir uma sistemática que inclui: identificação do problema, identificação das possíveis causas, identificação das causas-raiz, o que será feito, 
            responsável pela execução, data de início e fim, 
          planejamento das ações, execução, acompanhamento <i>(follow-up)</i>, análise dos resultados obtidos e reelaboração do plano, ou a padronização do processo.
           Importante registrar o status. Para um plano de ação consistente, é necessário seguir detalhadamente cada uma das etapas.
        </p>
        <p class="modal-text">
          4. Identificação do problema. Mapear os indicadores que não estão atingindo a meta proposta ou os processos que não estão sendo cumpridos.
        </p>
        <p class="modal-text">
          5.Identificação das possíveis causas. Realizar análises detalhadas dos dados para cada problema mapeado e entender onde estão as lacunas de cada um, 
          podendo ser em um departamento da empresa, uma área de atendimento, um cliente ou rede, equipe, segmento, indústria,
           produto ou até mesmo a própria execução de um processo. Importante identificar o maior número possível de causas para cada problema.
        </p>
        <p class="modal-text">
          6. Identificação da causa-raiz. Com base nas possíveis causas, utilizar a metodologia dos “5 Porquês” para identificar a causa-raiz de cada um dos problemas mapeados. Para isso, deve-se questionar “porquê” repetidas vezes, sempre sobre o efeito do “porquê” anterior.<br>
          <br>
          Em alguns casos, podem ser necessários mais ou menos “porquês”, portanto, explore o máximo da metodologia. Para uma análise mais assertiva, outras questões podem ser consideradas durante o questionamento dos “porquês”:
          <br>
          <br>
          • Que evidências tenho de que esta causa existe? (É concreta? É mensurável?)<br>
          <br>
          • Que evidências tenho de que esta causa leva ao problema identificado? (ou estou apenas fazendo suposições?)<br>
          <br>
          • Que evidências tenho de que esta é a principal causa que verdadeiramente leva ao problema? (mesmo que seja um fator importante, a causa principal poderia ser outra).<br>
          <br>
          • Algo mais deve ocorrer junto a esta causa para que o problema ocorra? (serve para esclarecer se o problema não vem de uma combinação de fatores).
        </p>
        <p class="modal-text">
          7. Matriz GUT. É uma ferramenta de auxílio para definir as prioridades dos problemas, classificando-os conforme sua Gravidade, Urgência e Tendência (GUT):<br>
          <br>
          • Gravidade – é analisada considerando o quanto as consequências dos problemas afetarão o andamento dos processos e o atingimento dos objetivos estratégicos da EBD.<br>
          <br>
          • Urgência – é analisada considerando o tempo para resolução dos problemas em relação ao seu impacto pela demora dessa resolução.<br>
          <br>
          • Tendência – é analisada considerando qual a probabilidade de os problemas ficarem piores se não forem resolvidos imediatamente.
        </p>
        <p class="modal-text">
          8. Utilize uma planilha (Matriz GUT) para automatizar e organizar a priorização de atuação sobre os problemas mapeados. Essa planilha deve compreender os seguintes passos:<br>
          <br>
          • Liste os problemas.<br>
          <br>
          • Pontue, dando notas de 1 (baixa intensidade) a 5 (alta intensidade) para cada uma das três variáveis (Gravidade, Urgência e Tendência).<br>
          <br>
          • Multiplique as três notas para obter o resultado GUT de cada problema.<br>
          <br>
          • Classifique os problemas, em ordem decrescente, utilizando os resultados GUT.<br>
          <br>
          • Construa um Pareto para definir quais problemas têm maior impacto sobre o negócio e terão atuação imediata.
        </p>
        <p class="modal-text">
          9. Pareto. O princípio de Pareto considera que 80% das consequências são geradas de 20% das causas. Sua maior utilidade é a de permitir uma fácil visualização e identificação dos problemas mais importantes, possibilitando a concentração de esforços. Com base na Matriz GUT, é possível construir um gráfico de Pareto. Para isso, é necessário incluir os seguintes passos na elaboração do arquivo:<br>
          <br>
          • Calcule o percentual da representatividade (peso) de cada problema sobre o total (dividir o valor obtido para cada problema pela soma dos valores de todos os problemas).<br>
          <br>
          • Calcule o percentual acumulado de cada problema (somar o percentual obtido para cada problema com o percentual do problema anterior). <br>
          <br>
          • Selecione os problemas que, somados, representam 80% das causas. Para facilitar esse entendimento, também é possível construir um gráfico (Histograma) de colunas para os valores de cada problema (eixo vertical principal), com linha para os percentuais acumulados (eixo vertical secundário).
        </p>
