          • Todo o final de mês, estabeleça metas/objetivos, micro e macro, que serão executadas no mês seguinte.<br>
          <br>
          • Definir as ações para o atingimento desses objetivos.<br>
          <br>
          • Distribuir cada uma delas no decorrer das semanas que seguem, e por fim para cada dia da semana.<br>
          <br>
          Dessa forma, é possível ter uma visão de tudo o que é importante e será mais fácil organizar as tarefas “menores” que surgirão ao longo do
          tempo, sem que isso afete o seu planejamento. Importante reforçar que todas essas metas mensais devem ser preestabelecidas de acordo com o
          planejado para o ano.
        </p>
        <p class="modal-text">
          3. Incluir na agenda as atividades “fixas” imprescindíveis para o mês conforme descrito para cada função.
