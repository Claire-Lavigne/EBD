        <h4 class="modal-subtitle">Passo a Passo do POP :</h4>
        <p class="modal-text">
          1. O gestor regional deverá participar mensalmente das reuniões, em no mínimo uma filial, além de garantir presença de no mínimo uma vez a cada quadrimestre em cada filial de sua responsabilidade. Para isso, o gestor regional deve definir previamente, um calendário, com as datas em que participará das reuniões de planejamento tático em cada filial, garantindo que todos os gestores reservem esse momento em sua agenda.
        </p>
        <p class="modal-text">
          2. Os gestores da filial ou o gestor regional deve definir quem irá presidir as reuniões ao longo do ano e garantir que um dos participantes da reunião fique responsável por redigir a ata com o registro de tudo o que foi discutido, assim como as ações que serão tomadas na execução dos planos de ação. Isso não apenas garantirá que todos estejam de acordo com os temas discutidos, mas também que haja resultados práticos daquilo que foi definido em grupo.
        </p>
        <p class="modal-text">
          3. O gestor responsável por presidir as reuniões em conjunto com o gestor regional deve definir a pauta da reunião em ordem de importância, o horário de início e fim para cada reunião, e não ocupar mais tempo que o necessário para que as decisões principais sejam tomadas.
        </p>
        <p class="modal-text">
          4. Essa programação deve ser compartilhar, por e-mail, com todos os participantes (gestores das áreas). Isso garantirá que os assuntos mais importantes sejam discutidos primeiro.
        </p>
        <p class="modal-text">
          5. Todos os gestores devem se preparar para discutir sobre os assuntos que serão abordados na reunião.
        </p>
        <p class="modal-text">
          6. Mensalmente, os gestores de cada área da filial devem realizar as reuniões de planejamento tático, para discutir os principais Itens de Controle (ICs), conforme descrito no <b>POP – EBD – Plano de Ação</b>, principalmente os que têm ou sofrem impactos de outras áreas, além dos direcionamentos do gestor regional.
        </p>
        <p class="modal-text">
          7. Essas reuniões devem ser iniciadas com o follow-up dos assuntos abordados nas reuniões anterior que originaram os planos de ação, apresentando seu atingimento e se necessário definir se o mesmo plano deverá continuar sendo executado ou se requer uma redefinição do plano de ação, caso este não tenha apresentado evolução sobre o objetivo proposto. Os planos de ação deverão sofrer alterações, sempre que for necessário, para garantir a consistência e efetividade dos planos.
        </p>
        <p class="modal-text">
          8. As tomadas das decisões devem ser objetivas e o gestor responsável por presidir as reuniões deve administrar o tempo, dando foco no que há de mais importante a ser discutido sobre a pauta principal.
